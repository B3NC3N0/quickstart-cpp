unsigned char *ImageData::UnpackData(const BmpImage &bmp_image) {
  int buffer_size = bmp_image.width * bmp_image.height * 4;
  unsigned char *image_buffer = new unsigned char[buffer_size];
  unsigned char *buffer_ptr = image_buffer;
  unsigned char *buffer_limit = buffer_ptr + buffer_size;

  const unsigned char *rle_ptr = bmp_image.rle_data;

  while (buffer_ptr < buffer_limit) {
    unsigned int current_byte = *(rle_ptr++);
    if (current_byte & 128) {
      int repeat_count = current_byte - 128;
      for (int i = 0; i < repeat_count * 4; i++) {
        *buffer_ptr = *(rle_ptr + (i % 4));
        buffer_ptr++;
      }
      rle_ptr += 4;
    } else {
      // current byte represents the length of the raw data blob we should copy.
      for (int i = 0; i < current_byte * 4; i++) {
        *buffer_ptr = *rle_ptr;
        buffer_ptr++;
        rle_ptr++;
      }
    }
  }
  return image_buffer;
}

